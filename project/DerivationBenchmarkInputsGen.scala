object DerivationBenchmarkInputsGen {

  def files(layerCount: Int): Seq[(String, String)] = {
    require(layerCount >= 3, "layerCount must be at least 3")

    Seq(
      "DerivationBenchmarkTypes.scala" -> typesFileContent(layerCount),
      "DerivationBenchmarkDerived.scala" -> derivedFileContent(layerCount),
      "DerivationBenchmarkAuto.scala" -> autoFileContent(layerCount),
    )
  }

  private def typesFileContent(layerCount: Int): String = {
    val layerDefinitions =
      (0 until layerCount).map(layerDefinition).mkString("\n\n")
    val currentLayer = s"BenchmarkLayer${layerCount - 1}"
    val previousLayer = s"BenchmarkLayer${layerCount - 2}"
    val baselineLayer = s"BenchmarkLayer${layerCount - 3}"

    s"""package difflicious.derivation
       |
       |// Generated by DerivationBenchmarkInputsGen. Run `sbt --client "benchmarkCompile / generateCompileBenchmarkSources"`.
       |object DerivationBenchmarkTypes {
       |
       |$layerDefinitions
       |
       |  final case class DerivationBenchmarkScenario(
       |    current: $currentLayer,
       |    previous: $previousLayer,
       |    baseline: $baselineLayer,
       |    labels: List[String],
       |    revision: Long,
       |  )
       |}
       |""".stripMargin
  }

  private def derivedFileContent(layerCount: Int): String = {
    val lastLayer = s"BenchmarkLayer${layerCount - 1}"
    val dependencyInstances =
      derivedDependencyTypeNames(layerCount)
        .map { typeName =>
          s"""  private implicit def ${instanceName(typeName)}: Differ[$typeName] =
             |    Differ.derived[$typeName]""".stripMargin
        }
        .mkString("\n\n")
    val lastLayerDerivations =
      (1 to 3)
        .map { index =>
          s"""  def ${instanceName(lastLayer)}$index: Differ[$lastLayer] =
             |    Differ.derived[$lastLayer]""".stripMargin
        }
        .mkString("\n\n")

    s"""package difflicious.derivation
       |
       |// Generated by DerivationBenchmarkInputsGen. Run `sbt --client "benchmarkCompile / generateCompileBenchmarkSources"`.
       |import difflicious.Differ
       |import difflicious.derivation.DerivationBenchmarkTypes._
       |
       |object DerivationBenchmarkDerived {
       |
       |$dependencyInstances
       |
       |$lastLayerDerivations
       |
       |  def derivationBenchmarkScenarioDiffer: Differ[DerivationBenchmarkScenario] =
       |    Differ.derived[DerivationBenchmarkScenario]
       |}
       |""".stripMargin
  }

  private def autoFileContent(layerCount: Int): String = {
    val lastLayer = s"BenchmarkLayer${layerCount - 1}"
    val lastLayerSummons =
      (1 to 3)
        .map { index =>
          s"""  def ${instanceName(lastLayer)}$index: Differ[$lastLayer] =
           |    Differ[$lastLayer]""".stripMargin
        }
        .mkString("\n\n")

    s"""package difflicious.derivation
       |
       |// Generated by DerivationBenchmarkInputsGen. Run `sbt --client "benchmarkCompile / generateCompileBenchmarkSources"`.
       |import difflicious.Differ
       |import difflicious.derivation.DerivationBenchmarkTypes._
       |import difflicious.generic.auto.given
       |
       |object DerivationBenchmarkAuto {
       |
       |$lastLayerSummons
       |}
       |""".stripMargin
  }

  private def derivedDependencyTypeNames(layerCount: Int): Seq[String] =
    (0 until layerCount).map(index => s"BenchmarkLayer$index")

  private def autoDependencyTypeNames(layerCount: Int): Seq[String] =
    (0 until (layerCount - 1)).map(index => s"BenchmarkLayer$index")

  private def instanceName(typeName: String): String = {
    val name = typeName.split('.').mkString
    name.head.toLower + name.tail + "Differ"
  }

  private def layerDefinition(index: Int): String = {
    val fields =
      if (index == 0) {
        s"""    strs: List[String],""".stripMargin
      } else {
        val previous = s"BenchmarkLayer${index - 1}"

        s"""    previous0: $previous,
           |    previous1: $previous,
           |    previous2: $previous,
           |    previous3: $previous,
           |    strs: List[String],""".stripMargin
      }

    s"""  final case class BenchmarkLayer$index(
       |$fields
       |  )""".stripMargin
  }
}
